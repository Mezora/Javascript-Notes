// Date object
let d;
d = new Date();
console.log(d.getFullYear()); // 2021
console.log(d.getDay()) // 2 = Tuesday
console.log(d.getMonth() + 1) // 4 = April
console.log(d.getMinutes()) // 59
console.log(d.getHours()) // 19 = 7pm
console.log(d.getSeconds()) // 43s

let d2 = new Date('2004, 11, 11');
console.log(d2); // 11 / 11 / 2004